#include "minirt.h"

t_matrix	m_multiply(t_matrix m1, t_matrix m2)
{
	t_matrix	m;
	int			i;
	int			j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m[i][j] = m1[i][0] * m2[0][j] + m1[i][1] * m2[1][j] + \
					m1[i][2] * m2[2][j] + m1[i][3] * m2[3][j];
			j++;
		}
		i++;
	}
}
